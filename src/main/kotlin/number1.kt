fun main() {

    val a: Int = 10000
    val d: Double = 100.00
    val f: Float = 100.00f
    val l: Long = 1000000004L
    val s: Short = 10
    val b: Byte = Byte.MAX_VALUE

    println("Int Value is " + a)
    println("Double  Value is " + d)
    println("Float Value is " + f)
    println("Long Value is " + l)
    println("Short Value is " + s)
    println("Byte Value is " + b)

}